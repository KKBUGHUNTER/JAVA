package javaprogramI;

import java.util.Scanner;

public class JavaStdInStdOut {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int a, b, c;
		a = sc.nextInt();
		b = sc.nextInt();
		c = sc.nextInt();

		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
	}
}
